GPIO EQU 0x06
LOOP:
  MOVLW 'H'
  CALL PUSH
  MOVLW 'e'
  CALL PUSH
  MOVLW 'l'
  CALL PUSH
  MOVLW 'l'
  CALL PUSH
  MOVLW 'o'
  CALL PUSH
  MOVLW ','
  CALL PUSH
  MOVLW ' '
  CALL PUSH
  MOVLW 'W'
  CALL PUSH
  MOVLW 'o'
  CALL PUSH
  MOVLW 'r'
  CALL PUSH
  MOVLW 'l'
  CALL PUSH
  MOVLW 'd'
  CALL PUSH
  MOVLW '!'
  CALL PUSH
  MOVLW '\n'
  CALL PUSH
  GOTO LOOP

PUSH:
  MOVWF GPIO
  BSF GPIO, 7
  BCF GPIO, 7
  RETLW 0
